
public class Vida {
	private int NivelDeEnergia;
	private int NumeroDeVidas;
	private int CapacidadOfensiva;
	public int getNivelDeEnergia() {
		return NivelDeEnergia;
	}
	public void setNivelDeEnergia(int nivelDeEnergia) {
		NivelDeEnergia = nivelDeEnergia;
	}
	public int getNumeroDeVidas() {
		return NumeroDeVidas;
	}
	public void setNumeroDeVidas(int numeroDeVidas) {
		NumeroDeVidas = numeroDeVidas;
	}
	public int getCapacidadOfensiva() {
		return CapacidadOfensiva;
	}
	public void setCapacidadOfensiva(int capacidadOfensiva) {
		CapacidadOfensiva = capacidadOfensiva;
	}

}
